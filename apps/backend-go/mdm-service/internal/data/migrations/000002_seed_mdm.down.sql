WHERE key IN (
  'DEFAULT_LANGUAGE',
  'DEFAULT_TIMEZONE',
  'SESSION_TIMEOUT_MINUTES',
  'ENABLE_MAINTENANCE_MODE',
  'FINANCE_DEFAULT_CURRENCY',
  'BANKING_BUSINESS_DATE_MODE'
);

